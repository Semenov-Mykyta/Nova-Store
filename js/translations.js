/* Make translations available for other scripts such as cart.js */
window.translations = translations;

/* Currently active language, persisted in localStorage across page loads */
let currentLang = localStorage.getItem("lang") || "en";

function getTranslationDictionary(lang = currentLang) {
    return window.translations[lang] || window.translations.en;
}

/**
 * Returns translated text by key and replaces placeholders like {orderId}.
 */
function translate(key, params = {}) {
    const dict = getTranslationDictionary(currentLang);
    let text = dict[key] || window.translations.en[key] || key;

    Object.entries(params).forEach(([name, value]) => {
        text = text.replaceAll(`{${name}}`, String(value ?? ""));
    });

    return text;
}

window.translate = translate;
window.t = translate;

/**
 * Applies translations to every element on the page that has a data-i18n attribute.
 */
function applyTranslations() {
    const dict = getTranslationDictionary(currentLang);

    document.querySelectorAll("[data-i18n]").forEach((el) => {
        const key = el.getAttribute("data-i18n");
        const translated = dict[key] || window.translations.en[key];

        if (translated) el.textContent = translated;
    });

    document.querySelectorAll("[data-i18n-placeholder]").forEach((el) => {
        const key = el.getAttribute("data-i18n-placeholder");
        const translated = dict[key] || window.translations.en[key];

        if (translated) el.setAttribute("placeholder", translated);
    });

    const langLabel = document.getElementById("lang-label");
    if (langLabel) langLabel.textContent = currentLang.toUpperCase();
}

window.applyTranslations = applyTranslations;

/**
 * Changes the site language, saves the choice and refreshes dynamic content.
 */
function setLanguage(lang) {
    currentLang = lang === "de" ? "de" : "en";
    localStorage.setItem("lang", currentLang);

    applyTranslations();

    if (typeof renderShopProducts === "function" && document.getElementById("shop-products")) {
        renderShopProducts();
    }

    if (typeof renderFeaturedProducts === "function" && document.getElementById("featured-products")) {
        renderFeaturedProducts();
    }

    if (typeof initProductPage === "function" && document.querySelector(".product-layout")) {
        initProductPage();
    }
}

window.setLanguage = setLanguage;

document.addEventListener("DOMContentLoaded", () => {
    applyTranslations();

    const langToggle = document.getElementById("lang-toggle");
    if (langToggle) {
        langToggle.addEventListener("click", () => {
            setLanguage(currentLang === "en" ? "de" : "en");
        });
    }
});
